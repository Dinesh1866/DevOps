// difference between let , const and var in javascript
// let and const are block scoped
// var is function scoped
// let and const are not hoisted
// var is hoisted
// let and const are not accessible before declaration
// var is accessible before declaration
// let and const are not re-declared
// var is re-declared
// let and const are not re-assigned
// var is re-assigned
// let and const are not initialized
// var is initialized
// let and const are not global
// var is global
// let and const are not mutable
// var is mutable
// let and const are not immutable
// var is immutable
// let and const are not primitive
// var is primitive
// let and const are not object
// var is object

