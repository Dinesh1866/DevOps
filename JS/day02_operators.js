//Operators
var a = 10;
var b = 20;

//arithmetic operators
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(++a);
console.log(a--);
console.log(a);
console.log(a++);
console.log(a);


//comparison operators
console.log(a == b);
console.log(a != b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);


//logical operators
console.log(a > 5 && b > 5);
console.log(a > 5 || b > 5);
console.log(!(a > 5 && b > 5));

//ternary operator
console.log(a > 5 ? "a is greater than 5" : "a is less than 5");

//assignment operators
console.log(a += b);
console.log(a -= b);
console.log(a *= b);
console.log(a /= b);
console.log(a %= b);