//conditional Statements
//Decision making statements are used to decide the flow of execution of the program.

//if
var a = 10;
var b = 20;
if (a > b) {
      console.log("a is greater than b");
      }


//if else
var a = 10;
var b = 20;
if (a > b) {
      console.log("a is greater than b");
      }
else {
      console.log("b is greater than a");
      }


//if else if
var a = 10;
var b = 20;
if (a > b) {
      console.log("a is greater than b");
      }
else if (a < b) {
      console.log("b is greater than a");
      }
else {
      console.log("a and b are equal");
      }


//switch
var a = 10;
switch (a) {
      case 10:
            console.log("a is 10");
            break;
      case 20:
            console.log("a is 20");
            break;
      default:
            console.log("a is not 10 or 20");
            break;
      }
